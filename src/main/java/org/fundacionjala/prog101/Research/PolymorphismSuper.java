package org.fundacionjala.prog101.Research;

/**
 * Class to prove polymorphism.
 */
public class PolymorphismSuper {
    /**
     * Method to printValue String
     * @param value
     */
    public void printValue(final String value) {
        System.out.println(value);
    }
    /**
     * Method to printValue String
     * @param value
     */
    public void printValue(final int value) {
        System.out.println(value);
    }
    /**
     * Method to printValue String
     * @param value
     */
    public void printValue(final double value) {
        System.out.println(value);
    }
}

